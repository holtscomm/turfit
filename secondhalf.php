    <p>On-site amenities such as the concession, public washrooms, and locker rooms will undergo significant changes, and the addition of a clubhouse could increase the facility's usability year-round.
Installation of artificial turf in the Bowl at Gordon Howe Park will not only improve the quality of the field's surface to address safety concerns, but will also add considerable capacity to Saskatoon's sports field inventory.</p>
<p>The existing natural grass surface follows a city maintenance schedule that limits field access to only four months per year, resulting in a five-fold increase to field rental cost compared to other local turf fields and making sufficient practice time unaffordable for many clubs. The only alternative is overuse, which compromises the safety of our athletes in order to meet demands. However, an artificial turf field is safe for activity come rain or shine and maintenance crews can remove snow from it without causing damage. This makes for longer seasons, with fewer cancelled games and practices due to bad weather.</p>
<p>Teams will also be able to extend their training schedules; sport camps could start as early as the beginning of March and continue into late November. Furthermore, the facility could be booked by user groups for ticketed events such as tournaments. This will allow the collection of gate revenue to help defray costs - a benefit not readily available to many outdoor sport clubs through existing venues.</p>
<p><strong>With your help, the creation of an outdoor, well-lit, multi-sport facility with artificial turf will provide the much-needed capacity for a growing city, and celebrate the achievements of a vibrant and active football community!</strong></p>
